#include "libft.h"

int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	while (i < nb / 2)
	{
		if (i * i == nb)
		{
			if (nb % i == 0)
				return (i);
		}
		i++;
	}
	return (0);
}
